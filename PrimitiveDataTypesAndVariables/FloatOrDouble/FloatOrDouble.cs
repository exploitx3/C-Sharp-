using System;

namespace FloatOrDouble
{
    class FloatOrDouble
    {
        static void Main()
        {

            float f1 = 34.567839023f;
            float f2 = 12.345f;
            float f3 = 8923.1234857f;
            float f4 = 3456.091f;

            double d1 = 34.567839023;
            double d2 = 12.345;
            double d3 = 8923.1234857;
            double d4 = 3456.091;

            Console.WriteLine("Using float:\n" + f1 + "\n" + f2 + "\n" + f3 + "\n" + f4 + "\n");
            Console.WriteLine("Using double:\n" + d1 + "\n" + d2 + "\n" + d3 + "\n" + d4 + "\n");

        }
    }
}
